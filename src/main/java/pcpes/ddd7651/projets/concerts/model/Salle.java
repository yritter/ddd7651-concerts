package pcpes.ddd7651.projets.concerts.model;

public class Salle {

	private String nom;
	private int capacite;

	public Salle() {
		super();
	}

	public String getNom() {
		return nom;
	}

	public void setNom(String nom) {
		this.nom = nom;
	}

	public int getCapacite() {
		return capacite;
	}

	public void setCapacite(int capacite) {
		this.capacite = capacite;
	}	
}
